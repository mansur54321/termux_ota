    echo -e "${YELLOW}–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–µ—Ä–≤–∞–Ω–∞.${RESET}"
    exit 1
}

# --- –ù–ê–ß–ê–õ–û –°–ö–†–ò–ü–¢–ê ---
