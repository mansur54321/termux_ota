echo -e "${BLUE}==     –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ –¥–ª—è Realme OTA    ==${RESET}"
echo -e "${BLUE}=====================================================${RESET}"
echo ""
