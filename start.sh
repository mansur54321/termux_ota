#!/bin/bash

# --- –ù–ê–°–¢–†–û–ô–ö–ò ---
B_SH_URL="https://raw.githubusercontent.com/mansur54321/termux_ota/refs/heads/main/b.sh"
REALME_OTA_REPO_URL="https://github.com/R0rt1z2/realme-ota.git"
# --- –ö–û–ù–ï–¶ –ù–ê–°–¢–†–û–ï–ö ---

# –¶–≤–µ—Ç–∞ –¥–ª—è –∫—Ä–∞—Å–∏–≤–æ–≥–æ –≤—ã–≤–æ–¥–∞
GREEN="\e[32m"
YELLOW="\e[33m"
BLUE="\e[34m"
RED="\e[31m"
RESET="\e[0m"

# –¶–µ–ª–µ–≤—ã–µ –ø–∞–ø–∫–∏ –∏ –ø—É—Ç–∏
OTA_DIR="/storage/emulated/0/OTA"
REALME_OTA_DIR="$OTA_DIR/realme-ota"
B_SH_PATH="$OTA_DIR/b.sh"

# --- –§–£–ù–ö–¶–ò–ò ---

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –≤—ã–≤–æ–¥–∞ –æ—à–∏–±–∫–∏ –∏ –≤—ã—Ö–æ–¥–∞
handle_error() {
    echo -e "\n${RED}–û–®–ò–ë–ö–ê: $1${RESET}"
    echo -e "${YELLOW}–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø—Ä–µ—Ä–≤–∞–Ω–∞. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –∏—Å–ø—Ä–∞–≤—å—Ç–µ –ø—Ä–æ–±–ª–µ–º—É –∏ –∑–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å–Ω–æ–≤–∞.${RESET}"
    exit 1
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ –Ω–∞–ª–∏—á–∏—è –∫–æ–º–∞–Ω–¥—ã (—É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ –ø–∞–∫–µ—Ç–∞)
check_command() {
    command -v "$1" >/dev/null 2>&1 || handle_error "–ü–∞–∫–µ—Ç '$1' –Ω–µ –±—ã–ª —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –∏–ª–∏ –Ω–µ –Ω–∞–π–¥–µ–Ω –≤ PATH."
}

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω–æ–≥–æ Python-–º–æ–¥—É–ª—è
check_python_module() {
    python -c "import $1" &>/dev/null || handle_error "Python-–º–æ–¥—É–ª—å '$1' –Ω–µ –±—ã–ª —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω."
}


# --- –ù–ê–ß–ê–õ–û –°–ö–†–ò–ü–¢–ê ---
clear
echo -e "${BLUE}=====================================================${RESET}"
echo -e "${BLUE}==     –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏–π —É—Å—Ç–∞–Ω–æ–≤—â–∏–∫ –¥–ª—è Realme OTA    ==${RESET}"
echo -e "${BLUE}=====================================================${RESET}"
echo ""
echo -e "${YELLOW}–≠—Ç–æ—Ç —Å–∫—Ä–∏–ø—Ç –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —Å–∫–∞—á–∞–µ—Ç –∏ –Ω–∞—Å—Ç—Ä–æ–∏—Ç –≤—Å—ë –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ–µ.${RESET}"
read -p "–ù–∞–∂–º–∏—Ç–µ [Enter] –¥–ª—è –Ω–∞—á–∞–ª–∞ —É—Å—Ç–∞–Ω–æ–≤–∫–∏..."

# --- –®–∞–≥ 1: –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ö—Ä–∞–Ω–∏–ª–∏—â–∞ –∏ —Å–æ–∑–¥–∞–Ω–∏–µ –ø–∞–ø–æ–∫ ---
echo -e "\n${GREEN}>>> –®–∞–≥ 1: –ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ö—Ä–∞–Ω–∏–ª–∏—â–∞...${RESET}"
termux-setup-storage
mkdir -p "$OTA_DIR" || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–æ–∑–¥–∞—Ç—å –ø–∞–ø–∫—É $OTA_DIR."
echo -e "${GREEN}–ü–∞–ø–∫–∞ $OTA_DIR –≥–æ—Ç–æ–≤–∞ –∫ —Ä–∞–±–æ—Ç–µ.${RESET}"

# --- –®–∞–≥ 2: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –ø–∞–∫–µ—Ç–æ–≤ ---
echo -e "\n${GREEN}>>> –®–∞–≥ 2: –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–∞–∫–µ—Ç–æ–≤ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π...${RESET}"
# –ò–°–ü–†–ê–í–õ–ï–ù–ò–ï: –î–æ–±–∞–≤–ª–µ–Ω–∞ –æ–ø—Ü–∏—è --force-confold –¥–ª—è –∞–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–æ–≥–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è
pkg update -y && pkg upgrade -y -o Dpkg::Options::="--force-confold"
pkg install -y curl git python tsu || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –±–∞–∑–æ–≤—ã–µ –ø–∞–∫–µ—Ç—ã."

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã—Ö —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –ø–∞–∫–µ—Ç–æ–≤
echo -e "\n${BLUE}–ü—Ä–æ–≤–µ—Ä–∫–∞ —Å–∏—Å—Ç–µ–º–Ω—ã—Ö –ø–∞–∫–µ—Ç–æ–≤...${RESET}"
check_command "curl"
check_command "git"
check_command "python"
check_command "tsu"
echo -e "${GREEN}–í—Å–µ —Å–∏—Å—Ç–µ–º–Ω—ã–µ –ø–∞–∫–µ—Ç—ã —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã.${RESET}"

# --- –®–∞–≥ 3: –ö–ª–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è realme-ota ---
echo -e "\n${GREEN}>>> –®–∞–≥ 3: –ó–∞–≥—Ä—É–∑–∫–∞ —É—Ç–∏–ª–∏—Ç—ã realme-ota...${RESET}"
if [ -d "$REALME_OTA_DIR" ]; then
    echo -e "${YELLOW}–ü–∞–ø–∫–∞ '$REALME_OTA_DIR' —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç. –û–±–Ω–æ–≤–ª—è–µ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π...${RESET}"
    cd "$REALME_OTA_DIR" || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å –ø–µ—Ä–µ–π—Ç–∏ –≤ –ø–∞–ø–∫—É $REALME_OTA_DIR"
    git pull || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å –æ–±–Ω–æ–≤–∏—Ç—å —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π —á–µ—Ä–µ–∑ 'git pull'."
    cd - > /dev/null
else
    echo -e "${BLUE}–ö–ª–æ–Ω–∏—Ä—É–µ–º —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π –∏–∑ GitHub...${RESET}"
    git clone "$REALME_OTA_REPO_URL" "$REALME_OTA_DIR" || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å –∫–ª–æ–Ω–∏—Ä–æ–≤–∞—Ç—å —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏–π."
fi
echo -e "${GREEN}–£—Ç–∏–ª–∏—Ç–∞ realme-ota —É—Å–ø–µ—à–Ω–æ –∑–∞–≥—Ä—É–∂–µ–Ω–∞ –≤ $REALME_OTA_DIR.${RESET}"

# --- –®–∞–≥ 4: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Python-–º–æ–¥—É–ª–µ–π ---
echo -e "\n${GREEN}>>> –®–∞–≥ 4: –£—Å—Ç–∞–Ω–æ–≤–∫–∞ Python-–º–æ–¥—É–ª–µ–π...${RESET}"
pip install --upgrade pip wheel

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏
pip install --upgrade pycryptodome requests || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å pycryptodome –∏–ª–∏ requests."

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Å–∞–º—É —É—Ç–∏–ª–∏—Ç—É –∏–∑ –ª–æ–∫–∞–ª—å–Ω–æ–π –ø–∞–ø–∫–∏
pip install --upgrade "$REALME_OTA_DIR" || handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å realme-ota –∏–∑ –ª–æ–∫–∞–ª—å–Ω–æ–π –ø–∞–ø–∫–∏."

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—ã—Ö Python-–º–æ–¥—É–ª–µ–π
echo -e "\n${BLUE}–ü—Ä–æ–≤–µ—Ä–∫–∞ Python-–º–æ–¥—É–ª–µ–π...${RESET}"
check_python_module "Crypto" # pycryptodome –∏–º–ø–æ—Ä—Ç–∏—Ä—É–µ—Ç—Å—è –∫–∞–∫ Crypto
check_python_module "requests"
check_python_module "realme_ota"
echo -e "${GREEN}–í—Å–µ Python-–º–æ–¥—É–ª–∏ —É—Å–ø–µ—à–Ω–æ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã.${RESET}"

# --- –®–∞–≥ 5: –ó–∞–≥—Ä—É–∑–∫–∞ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Å–∫—Ä–∏–ø—Ç–∞ b.sh ---
echo -e "\n${GREEN}>>> –®–∞–≥ 5: –ó–∞–≥—Ä—É–∑–∫–∞ –æ—Å–Ω–æ–≤–Ω–æ–≥–æ —Å–∫—Ä–∏–ø—Ç–∞ (b.sh)...${RESET}"
curl -sL "$B_SH_URL" -o "$B_SH_PATH"
if [ ! -f "$B_SH_PATH" ] || [ ! -s "$B_SH_PATH" ]; then
    handle_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —Å–∫—Ä–∏–ø—Ç b.sh! –ü—Ä–æ–≤–µ—Ä—å—Ç–µ URL –∏ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç-—Å–æ–µ–¥–∏–Ω–µ–Ω–∏–µ."
fi
echo -e "${GREEN}–°–∫—Ä–∏–ø—Ç —É—Å–ø–µ—à–Ω–æ –∑–∞–≥—Ä—É–∂–µ–Ω –≤ $B_SH_PATH${RESET}"


# --- –®–∞–≥ 6: –°–æ–∑–¥–∞–Ω–∏–µ —è—Ä–ª—ã–∫–∞ ---
echo -e "\n${GREEN}>>> –®–∞–≥ 6: –°–æ–∑–¥–∞–Ω–∏–µ —è—Ä–ª—ã–∫–∞ –¥–ª—è –≤–∏–¥–∂–µ—Ç–∞...${RESET}"
SHORTCUT_DIR="$HOME/.shortcuts"
SHORTCUT_FILE="$SHORTCUT_DIR/Realme_OTA"

mkdir -p "$SHORTCUT_DIR"
chmod 700 -R "$SHORTCUT_DIR"

# –°–æ–∑–¥–∞–µ–º —Å–∫—Ä–∏–ø—Ç-–∑–∞–ø—É—Å–∫–∞—Ç–æ—Ä, –∫–æ—Ç–æ—Ä—ã–π —É–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞ —Å–∫–∞—á–∞–Ω–Ω—ã–π b.sh
echo "#!/bin/bash" > "$SHORTCUT_FILE"
echo "bash $B_SH_PATH" >> "$SHORTCUT_FILE"
chmod +x "$SHORTCUT_FILE"
echo -e "${GREEN}–Ø—Ä–ª—ã–∫ 'Realme_OTA' —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω.${RESET}"

# --- –ó–ê–í–ï–†–®–ï–ù–ò–ï ---
clear
echo -e "${GREEN}=============================================${RESET}"
echo -e "${GREEN}      üéâ –£—Å—Ç–∞–Ω–æ–≤–∫–∞ —É—Å–ø–µ—à–Ω–æ –∑–∞–≤–µ—Ä—à–µ–Ω–∞! üéâ      ${RESET}"
echo -e "${GREEN}=============================================${RESET}"
echo ""
echo -e "${YELLOW}–ß—Ç–æ –¥–µ–ª–∞—Ç—å –¥–∞–ª—å—à–µ:${RESET}"
echo "1. –ü–æ–ª–Ω–æ—Å—Ç—å—é –∑–∞–∫—Ä–æ–π—Ç–µ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ Termux (–∫–æ–º–∞–Ω–¥–æ–π 'exit' –∏–ª–∏ —á–µ—Ä–µ–∑ –º–µ–Ω—é)."
echo "2. –ü–µ—Ä–µ–π–¥–∏—Ç–µ –Ω–∞ –≥–ª–∞–≤–Ω—ã–π —ç–∫—Ä–∞–Ω –≤–∞—à–µ–≥–æ —Ç–µ–ª–µ—Ñ–æ–Ω–∞."
echo "3. –î–æ–±–∞–≤—å—Ç–µ –≤–∏–¥–∂–µ—Ç 'Termux'."
echo "4. –í —Å–ø–∏—Å–∫–µ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —è—Ä–ª—ã–∫–æ–≤ –¥–æ–ª–∂–µ–Ω –ø–æ—è–≤–∏—Ç—å—Å—è 'Realme_OTA'."
echo "5. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ –Ω–µ–≥–æ, —á—Ç–æ–±—ã –∑–∞–ø—É—Å—Ç–∏—Ç—å —Å–∫—Ä–∏–ø—Ç –ø–æ–∏—Å–∫–∞ –æ–±–Ω–æ–≤–ª–µ–Ω–∏–π."
echo ""
echo -e "${BLUE}–£–¥–∞—á–∏!${RESET}"
